import json
import requests
import time

# Convert the result to a list of IDs
ids = [item["id"] for item in result["data"]["core_urlresult"]]

# 2. 設定每批次的大小
batch_size = 10

# 3. 修正後的 URL (注意末端不要斜槓)
base_url = "http://localhost:8000/api/scanners/vuln/urls_tech"

# 4. 使用 range 的步進功能進行分批處理
for i in range(230, len(ids), batch_size):
    # 取得當前批次的 ID 列表 (例如：0-10, 10-20...)
    batch = ids[i : i + batch_size]

    # 構造符合 NucleiScanURLByIdsSchema 的 JSON Payload
    payload = {"ids": batch}

    try:
        # 使用 json= 發送 POST 請求
        response = requests.post(base_url, json=payload)

        print(
            f"正在發送第 {i//batch_size + 1} 批次 (ID: {batch[0]}...{batch[-1]}): 狀態碼 {response.status_code}"
        )

        # 檢查是否發送成功
        if response.status_code != 202:
            print(f"警告：批次發送失敗！錯誤訊息: {response.text}")

        # (可選) 稍微停頓一下，避免瞬間衝擊 API (如果後端沒寫 Celery 隊列的話)
        time.sleep(3)

    except Exception as e:
        print(f"發生網路錯誤: {e}")

print("--- 所有任務派發完畢 ---")
